let img1=document.querySelector("#dbi16"),
p1=document.querySelector("#pdbi16");
function picview1()
{
    if(img1.style.opacity<1)
    {
        p1.style.color="rgb(38, 141, 134)";
        img1.style.opacity=1;
    }
    else
    {
        p1.style.color="black";
        img1.style.opacity=0.6;
    }
}
